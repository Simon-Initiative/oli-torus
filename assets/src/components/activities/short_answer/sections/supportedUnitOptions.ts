const commonCompoundUnitSet = new Set(commonCompoundUnits);
const displayValue = (value: string) => (value === '1' ? '1 (unitless)' : value);

export const supportedUnitOptions: SupportedUnitOption[] = [
  ...commonCompoundUnits.map((value) => ({
    value,
    displayValue: displayValue(value),
    group: 'Common compound units' as const,
  })),
  ...unitAtoms
    .filter((value) => !commonCompoundUnitSet.has(value))
    .map((value) => ({
      value,
      displayValue: displayValue(value),
      group: 'Supported unit atoms' as const,
    })),
];
